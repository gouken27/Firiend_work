# Задание: создать список планет, запросить название, вывести позицию или сообщение об отсутствии

planets = ["Меркурий", "Венера", "Земля", "Марс", "Юпитер", "Сатурн", "Уран", "Нептун"]

name = input("Введите название планеты: ").strip().title()

if name in planets:
    position = planets.index(name) + 1
    print(f"Планета {name} находится на {position}-м месте в Солнечной системе!")
else:
    print(f"К сожалению, планета «{name}» не входит в наш список.")